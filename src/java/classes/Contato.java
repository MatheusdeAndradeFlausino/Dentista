/**
 *
 * @author valderlei
 */
@Entity
@Table(name = "CONTATO")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Contato.findAll", query = "SELECT c FROM Contato c"),
    @NamedQuery(name = "Contato.findById", query = "SELECT c FROM Contato c WHERE c.id = :id"),
    @NamedQuery(name = "Contato.findByEmail", query = "SELECT c FROM Contato c WHERE c.email = :email"),
    @NamedQuery(name = "Contato.findByCel", query = "SELECT c FROM Contato c WHERE c.cel = :cel"),
    @NamedQuery(name = "Contato.findByTel", query = "SELECT c FROM Contato c WHERE c.tel = :tel"),
    @NamedQuery(name = "Contato.findByTelOpc", query = "SELECT c FROM Contato c WHERE c.telOpc = :telOpc")})
public class Contato implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    // @Pattern(regexp="[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?", message="E-mail inv√°lido")//if the field contains email address consider using this annotation to enforce field validation
    @Size(max = 255)
    @Column(name = "email")
    private String email;
    @Size(max = 20)
    @Column(name = "cel")
    private String cel;
    @Size(max = 20)
    @Column(name = "tel")
    private String tel;
    @Size(max = 20)
    @Column(name = "telOpc")
    private String telOpc;
    @OneToMany(mappedBy = "idContato")
    private Collection<Pessoa> pessoaCollection;
    @OneToMany(mappedBy = "idContato")
    private Collection<Clinica> clinicaCollection;
    @OneToMany(mappedBy = "idContato")
    private Collection<Empresa> empresaCollection;

    public Contato() {
    }

    public Contato(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCel() {
        return cel;
    }

    public void setCel(String cel) {
        this.cel = cel;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getTelOpc() {
        return telOpc;
    }

    public void setTelOpc(String telOpc) {
        this.telOpc = telOpc;
    }

    @XmlTransient
    public Collection<Pessoa> getPessoaCollection() {
        return pessoaCollection;
    }

    public void setPessoaCollection(Collection<Pessoa> pessoaCollection) {
        this.pessoaCollection = pessoaCollection;
    }

    @XmlTransient
    public Collection<Clinica> getClinicaCollection() {
        return clinicaCollection;
    }

    public void setClinicaCollection(Collection<Clinica> clinicaCollection) {
        this.clinicaCollection = clinicaCollection;
    }

    @XmlTransient
    public Collection<Empresa> getEmpresaCollection() {
        return empresaCollection;
    }

    public void setEmpresaCollection(Collection<Empresa> empresaCollection) {
        this.empresaCollection = empresaCollection;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Contato)) {
            return false;
        }
        Contato other = (Contato) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "classes.Contato[ id=" + id + " ]";
    }
    
}
